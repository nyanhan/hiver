####################################################   
### ; ### 命令分隔符可以用来在一行中来写多个命令####
####################################################

echo hello; echo there


# 等号左右不能有空格
filename=tmp.txt

# if [ 前后必须又空格 ]
if [ -f "$filename" ]; then # 注意:"if"和"then"需要分隔
    echo "File $filename exists."
else
    echo "File $filename not found."
fi; echo "File test complete."



####################################################
### ;; ### 终止 case 选项 ##########################
####################################################

case abc in
    abc) echo "\$variable = abc" ;; 
    xyz) echo "\$variable = xyz" ;; 
esac



####################################################
### . ##### == source ##############################
####################################################




####################################################
### , ### 都运行 只有最后一项返回 ##################
####################################################

let "t2=((a=9,15/3))"; echo $a; echo $t2



####################################################
### : ### 空命令 返回值为 0 ########################
####################################################


:
echo $?


while :
do
    echo ": == true"
    break
done

while true
do
    echo ": == true"
    break
done


if [ 3 == 3 ]
then :
else echo ": be placeholder."
fi


# empty filename
: > $filename
cat /dev/null > $filename


####################################################
### (= ==) 都是相等 != 不等 ########################
####################################################



####################################################
### () 命令组 独立作用域 | 数组 ####################
####################################################

a=123
( a=321;b=222 )
echo "\$a = $a"
echo "\$b = $b"


arr=(element1 element2 element3)
echo ${arr[1]}



####################################################
### {} 花括号扩展 | 代码块儿 #######################
####################################################

# 三个文件合并成一个
cat {file1,file2,file3} > $filename

fun(){
    local c;
    c=123; 
}

# 访问不到 local
echo "c = $c"

d=123
# 不影响作用域
{ d=321; }
echo "d = $d"
